package gdibike;

public class Cadastro {
  public String cpf;
  public String nome;
  public String telefone;
  public String endereco;
  public String imagem;
  
  public String getCpf() {
      return this.cpf;
  }
  public String getNome() {
      return this.nome;
  }

    public String getTelefone() {
        return telefone;
    }

    public String getEndereco() {
        return endereco;
    }

    public String getImagem() {
        return imagem;
    }
}